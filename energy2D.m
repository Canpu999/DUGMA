function F=energy2D(X)
   global A
   t1=X(1);
   t2=X(2);
   r11= X(3);
   r21= X(4);
   B=[r11^4, r11^3*t1, r11^3*t2, r11^3, r11^2*r21^2, r11^2*r21*t1, r11^2*r21*t2, r11^2*r21, r11^2*t1^2, r11^2*t1*t2, r11^2*t1, r11^2*t2^2, r11^2*t2, r11^2, r11*r21^2*t1, r11*r21^2*t2, r11*r21^2, r11*r21*t1^2, r11*r21*t1*t2, r11*r21*t1, r11*r21*t2^2, r11*r21*t2,r11*r21, r11*t1, r11*t2, r11, r21^4, r21^3*t1, r21^3*t2, r21^3, r21^2*t1^2, r21^2*t1*t2, r21^2*t1, r21^2*t2^2, r21^2*t2, r21^2, r21*t1, r21*t2, r21, t1^2, t1*t2, t1, t2^2, t2, 1];
   F=A*B';
end
